package com.demojam.demo;

public class Scores {
	
	public String Name;
	public String Score;
	/**
	 * @return the name
	 */
	public String getName() {
		return Name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		Name = name;
	}
	/**
	 * @return the score
	 */
	public String getScore() {
		return Score;
	}
	/**
	 * @param score the score to set
	 */
	public void setScore(String score) {
		Score = score;
	}

}
